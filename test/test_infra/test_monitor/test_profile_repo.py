# ------------------------------------------------------------------------------------------------ #
class ProfileGen:
    def __init__(self) -> None:
        self.idx = 0
        self.env = ["dev", "prod", "test"]
        self.stage = ["raw", "dqa", "clean", "features", "prep"]
        self.task_name = [
            "DataProfile",
            "DataQuality",
            "DataCleaning",
            "FeatureEngineering",
            "Final",
        ]

    def get_profile(self) -> TaskProfile:
        self.idx += 1
        return TaskProfile(
            env=self.env[self.idx % 3],
            stage=self.stage[self.idx % 5],
            task_name=self.task_name[self.idx % 5],
            task_start_time=datetime.now() - timedelta(days=random.randint(1, 4)),
            task_end_time=datetime.now() - timedelta(days=random.randint(4, 7)),
            runtime_seconds=random.randint(299, 500),
            cpu_cores=24,
            cpu_user_utilization=random.random(),
            cpu_system_utilization=random.random(),
            memory_usage_peak_mb=random.randint(1, 100),
            memory_allocations=random.random(),
            file_read_bytes=random.randint(1, 2000),
            file_write_bytes=random.randint(2, 2000),
            io_wait_time_seconds=random.randint(2, 2000),
            network_data_sent_bytes=random.randint(2, 2000),
            network_data_received_bytes=random.randint(2, 2000),
            exceptions_raised=random.randint(2, 20),
            retry_count=random.randint(1, 6),
        )


