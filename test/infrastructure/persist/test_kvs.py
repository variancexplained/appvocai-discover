import inspect
import logging
import os
from datetime import datetime

import pytest

from appinsight.infrastructure.persist.object.kvs import KVS

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"

DEPARTMENT = "unittest"
NAME = "kvs"

keys = [f"key_{i}" for i in range(10)]
values = [f"value_{i}" for i in range(10)]


@pytest.mark.kvs
class TestKVS:  # pragma: no cover
    # ============================================================================================ #
    def test_create(self, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        kvs = KVS(department=DEPARTMENT, name=NAME)
        for k, v in zip(keys, values):
            kvs.create(key=k, value=v)

        assert os.path.exists("workspace/test/kvs/unittest/kvs")

        with pytest.raises(FileExistsError):
            kvs.create(key=keys[0], value=values[0])
        assert len(kvs) == 10
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_read(self, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        kvs = KVS(department=DEPARTMENT, name=NAME)
        assert kvs.read(key=keys[5]) == values[5]
        assert kvs.read(key=keys[3]) != values[5]

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)

    # ============================================================================================ #
    def test_exists(self, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        kvs = KVS(department=DEPARTMENT, name=NAME)
        assert kvs.exists(key=keys[5])
        assert not kvs.exists(key="bogus")
        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
