from __future__ import annotations

from abc import ABC, abstractmethod

from discover.flow.base.stage import Stage


class StageBuilder(ABC):

    @property
    @abstractmethod
    def stage(self) -> Stage:
        pass

    @abstractmethod
    def build(self) -> StageBuilder:
        pass
